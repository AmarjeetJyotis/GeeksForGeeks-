//Function to count number of subtrees having sum equal to given sum.
int subtreeCount = 0;

// Function to calculate the sum of subtrees and count matching subtrees
int sumg(Node* root, int x) {
    if (!root) {
        return 0;
    }

    // Recursively get the sum of left and right subtrees
    int leftsum = sumg(root->left, x);
    int rightsum = sumg(root->right, x);

    // Calculate the total sum of the current subtree
    int subtreeSum = root->data + leftsum + rightsum;

    // If the subtree sum matches x, increment the count
    if (subtreeSum == x) {
        subtreeCount++;  // Use unique global variable
    }

    // Return the total sum of this subtree
    return subtreeSum;
}

// Wrapper function to count subtrees with sum X
int countSubtreesWithSumX(Node* root, int x) {
    subtreeCount = 0;  // Reset count before each function call
    sumg(root, x);
    return subtreeCount;
}